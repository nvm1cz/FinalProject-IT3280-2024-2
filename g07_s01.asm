handler: 
    # Saves the context 
    addi    sp, sp, -16 
    sw      a0, 0(sp) 
    sw      a1, 4(sp) 
    sw      a2, 8(sp) 
    sw      a7, 12(sp) 
     
    # Handles the interrupt 
    csrr    a1, ucause 
    li      a2, 0x7FFFFFFF 
    and     a1, a1, a2      # Clear interrupt bit to get the value 
     
    li      a2, MASK_CAUSE_TIMER 
    beq     a1, a2, timer_isr 
    li      a2, MASK_CAUSE_KEYPAD 
    beq     a1, a2, keypad_isr 
    j       end_process 
     
